
package uzay_seyahati;

public class Kisi {
    private String isim;
    private String uzayAraciAdi;

    public Kisi(String isim, String uzayAraciAdi) {
        this.isim = isim;
        this.uzayAraciAdi = uzayAraciAdi;
    }

    public String getIsim() {
        return isim;
    }

    public String getUzayAraciAdi() {
        return uzayAraciAdi;
    }
}
